struct SafetyTool: Identifiable, Hashable {
    let id: Int
    let title: String
    let group: String
    let focus: String

    var icon: String {
        switch group {
        case "作業前準備": return "checklist.checked"
        case "機械防護": return "gearshape.2.fill"
        case "車輛搬運": return "truck.box.fill"
        case "高處墜落": return "figure.fall"
        case "電氣安全": return "bolt.shield.fill"
        case "火災爆炸": return "flame.fill"
        case "化學衛生": return "testtube.2"
        case "局限空間": return "arrow.down.forward.and.arrow.up.backward"
        case "防護健康": return "cross.case.fill"
        case "應變管理": return "exclamationmark.shield.fill"
        default: return "wrench.and.screwdriver.fill"
        }
    }

    var checklist: [String] {
        [
            "確認「\(title)」適用的作業範圍、設備、人員資格與法規要求。",
            "現場核對重點：\(focus)。",
            "完成工程控制、隔離、警示、作業許可與個人防護措施。",
            "記錄照片、量測值、缺失責任人、改善期限與複驗結果。"
        ]
    }
}

